import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getPayment = () => new Promise((resolve, reject) => {
  fetch(`${endpoint}/payment.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const getSinglePayment = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/payments/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const createPayment = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/payment.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updatePayment = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/payment/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const deletePayment = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/payment/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

export {
  getPayment,
  getSinglePayment,
  createPayment,
  updatePayment,
  deletePayment,
};
